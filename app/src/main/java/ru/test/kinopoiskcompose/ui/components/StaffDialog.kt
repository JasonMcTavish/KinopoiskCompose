import ru.test.kinopoiskcompose.data.PROFESSIONS
import ru.test.kinopoiskcompose.db.model.FilmPersons

@OptIn(ExperimentalMaterial3Api::class)
@Composable
fun StaffDialog(
    title: String,
    onDismiss: () -> Unit,
    onPersonClick: (Int) -> Unit,
    personList: List<FilmPersons>
) {
    Dialog(
        onDismissRequest = onDismiss,
        properties = DialogProperties(
            usePlatformDefaultWidth = false,
            decorFitsSystemWindows = false
        )
    ) {
        Scaffold(
            topBar = {
                TopAppBar(
                    title = { Text(title, color = Color.White) },
                    navigationIcon = {
                        IconButton(onClick = onDismiss) {
                            Icon(
                                imageVector = Icons.AutoMirrored.Filled.ArrowBack,
                                contentDescription = "Назад",
                                tint = Color.White
                            )
                        }
                    },
                    colors = TopAppBarDefaults.topAppBarColors(
                        containerColor = Color.Transparent
                    )
                )
            }
        ) { innerPadding ->
            Column(
                modifier = Modifier
                    .fillMaxSize()
                    .padding(innerPadding)
            ) {
                LazyVerticalGrid(
                    columns = GridCells.Fixed(3),
                    modifier = Modifier.fillMaxSize(),
                    contentPadding = PaddingValues(8.dp),
                    horizontalArrangement = Arrangement.spacedBy(8.dp),
                    verticalArrangement = Arrangement.spacedBy(8.dp)
                ) {
                    items(personList) { person ->
                        PersonItem(
                            person = Person(
                                id = person.id,
                                name = person.name,
                                photo = person.poster,
                                role = PROFESSIONS.getValue(person.professionKey)
                            ),
                            onItemClick = { onPersonClick(person.id) }
                        )
                    }
                }
            }
        }
    }
}